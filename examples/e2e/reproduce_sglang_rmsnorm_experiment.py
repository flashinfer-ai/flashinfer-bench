"""Reproduce the SGLang fused_add_rmsnorm experiment matrix for Llama-3.1-8B."""

from __future__ import annotations

import argparse
import os
import shlex
import sys
from pathlib import Path
from typing import Any, Dict, List

from _compare_utils import load_json_artifact, load_json_file, run_command, write_csv, write_json

SCRIPT_DIR = Path(__file__).resolve().parent
PROBE_SCRIPT = SCRIPT_DIR / "sglang_fused_add_rmsnorm_probe.py"
APPLY_BOOTSTRAP = SCRIPT_DIR / "bootstrap_apply_runner.py"
FALLBACK_BOOTSTRAP = SCRIPT_DIR / "bootstrap_flashinfer_fallback_runner.py"
CUDA_SOLUTION_NAME = "gpt-5_fused_add_rmsnorm_h4096_cuda_optimized_r4_c0_high"


def build_parser() -> argparse.ArgumentParser:
    parser = argparse.ArgumentParser(
        description=(
            "Run the fused_add_rmsnorm_h4096 experiment matrix on SGLang + Llama-3.1-8B. "
            "Kernel latency compares FlashInfer baseline vs your CUDA kernel. "
            "E2E compares Original vs Fallback Substitution vs your CUDA kernel with apply()."
        )
    )
    parser.add_argument("--output-dir", required=True, help="Directory for logs and summaries")
    parser.add_argument("--python", default=sys.executable, help="Python executable used for subprocess runs")
    parser.add_argument("--model-path", required=True, help="Local path to the Llama-3.1-8B model")
    parser.add_argument("--trace-set-path", required=True, help="Trace-set root path")
    parser.add_argument(
        "--definition",
        default="fused_add_rmsnorm_h4096",
        help="Trace definition name to benchmark and apply",
    )
    parser.add_argument(
        "--solution-name",
        default=CUDA_SOLUTION_NAME,
        help="Exact generated solution name used for the direct CUDA-kernel benchmark",
    )
    parser.add_argument(
        "--batch-sizes",
        nargs="+",
        type=int,
        default=[1, 16, 64],
        help="Batch sizes to benchmark",
    )
    parser.add_argument("--device", default="cuda:0")
    parser.add_argument("--dtype", default="bfloat16", choices=["bfloat16", "float16"])
    parser.add_argument("--hidden-size", type=int, default=4096)
    parser.add_argument("--eps", type=float, default=1e-5)
    parser.add_argument("--kernel-warmup-runs", type=int, default=2)
    parser.add_argument("--kernel-benchmark-runs", type=int, default=10)
    parser.add_argument(
        "--input-len",
        type=int,
        default=16,
        help="SGLang bench_one_batch input length assumption",
    )
    parser.add_argument(
        "--output-len",
        type=int,
        default=1,
        help="SGLang bench_one_batch output length assumption",
    )
    parser.add_argument(
        "--max-atol",
        type=float,
        default=0.05,
        help="Relaxed atol used for A800 fused_add_rmsnorm traces",
    )
    parser.add_argument(
        "--max-rtol",
        type=float,
        default=0.01,
        help="Relaxed rtol used for A800 fused_add_rmsnorm traces",
    )
    parser.add_argument(
        "--trace-hardware-contains",
        default="A800",
        help="Hardware token used when selecting your CUDA kernel traces",
    )
    parser.add_argument(
        "--attention-backend",
        default="flashinfer",
        help="SGLang attention backend used for E2E runs",
    )
    parser.add_argument(
        "--sampling-backend",
        default="pytorch",
        help="SGLang sampling backend used for E2E runs",
    )
    parser.add_argument(
        "--disable-cuda-graph",
        action="store_true",
        default=True,
        help="Disable CUDA graph for E2E runs to reduce unrelated environment issues",
    )
    parser.add_argument(
        "--fib-cache-path",
        help="Optional override for FIB cache path used by apply-enabled runs",
    )
    parser.add_argument(
        "--flashinfer-workspace-base",
        default="/tmp/flashinfer_ws_fib_e2e",
        help=(
            "Workspace root for FlashInfer JIT cache. Using an isolated path avoids stale JIT "
            "artifacts generated by other Python environments."
        ),
    )
    return parser


def _common_bench_args(args: argparse.Namespace, batch_size: int, result_path: Path) -> List[str]:
    bench_args = [
        "--model-path",
        str(Path(args.model_path).resolve()),
        "--dtype",
        args.dtype,
        "--batch-size",
        str(batch_size),
        "--input-len",
        str(args.input_len),
        "--output-len",
        str(args.output_len),
        "--attention-backend",
        args.attention_backend,
        "--prefill-attention-backend",
        args.attention_backend,
        "--decode-attention-backend",
        args.attention_backend,
        "--sampling-backend",
        args.sampling_backend,
        "--result-filename",
        str(result_path),
    ]
    if args.disable_cuda_graph:
        bench_args.append("--disable-cuda-graph")
    return bench_args


def _build_run_env(args: argparse.Namespace) -> Dict[str, str]:
    env = os.environ.copy()
    if args.flashinfer_workspace_base:
        env["FLASHINFER_WORKSPACE_BASE"] = str(Path(args.flashinfer_workspace_base).resolve())
    return env


def _run_kernel_case(
    args: argparse.Namespace,
    *,
    batch_size: int,
    config_name: str,
    output_dir: Path,
) -> Dict[str, Any]:
    run_dir = output_dir / "kernel" / f"bs{batch_size}" / config_name
    run_dir.mkdir(parents=True, exist_ok=True)
    stdout_path = run_dir / "stdout.log"
    stderr_path = run_dir / "stderr.log"
    result_path = run_dir / "result.json"

    if config_name == "flashinfer_baseline":
        command = [
            args.python,
            str(PROBE_SCRIPT),
            "--entrypoint",
            "flashinfer",
            "--device",
            args.device,
            "--batch-size",
            str(batch_size),
            "--hidden-size",
            str(args.hidden_size),
            "--eps",
            str(args.eps),
            "--warmup-runs",
            str(args.kernel_warmup_runs),
            "--benchmark-runs",
            str(args.kernel_benchmark_runs),
            "--output-json",
            str(result_path),
        ]
        apply_summary_path = None
    elif config_name == "your_cuda_kernel":
        command = [
            args.python,
            str(PROBE_SCRIPT),
            "--entrypoint",
            "generated",
            "--trace-set-path",
            str(Path(args.trace_set_path).resolve()),
            "--definition",
            args.definition,
            "--solution-name",
            args.solution_name,
            "--batch-size",
            str(batch_size),
            "--device",
            args.device,
            "--hidden-size",
            str(args.hidden_size),
            "--eps",
            str(args.eps),
            "--warmup-runs",
            str(args.kernel_warmup_runs),
            "--benchmark-runs",
            str(args.kernel_benchmark_runs),
            "--output-json",
            str(result_path),
        ]
        apply_summary_path = None
    else:
        raise ValueError(f"Unsupported kernel config: {config_name}")

    run_info = run_command(
        command,
        cwd=SCRIPT_DIR,
        env=_build_run_env(args),
        stdout_path=stdout_path,
        stderr_path=stderr_path,
    )
    row: Dict[str, Any] = {
        "kind": "kernel",
        "config": config_name,
        "batch_size": batch_size,
        "status": run_info["status"],
        "exit_code": run_info["exit_code"],
        "duration_s": run_info["duration_s"],
        "command": shlex.join(command),
        "stdout_log": str(stdout_path),
        "stderr_log": str(stderr_path),
        "result_path": str(result_path),
        "apply_summary_json": str(apply_summary_path) if apply_summary_path else "",
    }
    if result_path.exists():
        payload = load_json_file(result_path)
        if config_name == "your_cuda_kernel":
            row.update(
                {
                    "latency_ms_avg": payload.get("latency_ms_avg"),
                    "latency_ms_min": payload.get("latency_ms_min"),
                    "latency_ms_max": payload.get("latency_ms_max"),
                    "selected_trace_solution": payload.get("entrypoint"),
                    "selected_trace_hardware": args.trace_hardware_contains,
                    "selected_trace_batch_size": payload.get("batch_size"),
                    "max_abs_error": payload.get("max_abs_error_output"),
                    "max_rel_error": None,
                }
            )
        else:
            row.update(
                {
                    "latency_ms_avg": payload.get("latency_ms_avg"),
                    "latency_ms_min": payload.get("latency_ms_min"),
                    "latency_ms_max": payload.get("latency_ms_max"),
                    "correct_output_atol_1e-2": payload.get("correct_output_atol_1e-2"),
                    "correct_residual_atol_1e-2": payload.get("correct_residual_atol_1e-2"),
                }
            )
    if apply_summary_path and apply_summary_path.exists():
        apply_summary = load_json_file(apply_summary_path)
        row["selected_solutions"] = ";".join(
            f"{item['solution']}:{item['calls']}" for item in apply_summary.get("selected_solutions", [])
        )
        row["table_hit_calls"] = apply_summary["dispatch_stats"]["table_hit_calls"]
        row["def_best_calls"] = apply_summary["dispatch_stats"]["def_best_calls"]
        row["fallback_calls"] = apply_summary["dispatch_stats"]["fallback_calls"]
    return row


def _run_e2e_case(
    args: argparse.Namespace,
    *,
    batch_size: int,
    config_name: str,
    output_dir: Path,
) -> Dict[str, Any]:
    run_dir = output_dir / "e2e" / f"bs{batch_size}" / config_name
    run_dir.mkdir(parents=True, exist_ok=True)
    stdout_path = run_dir / "stdout.log"
    stderr_path = run_dir / "stderr.log"
    result_path = run_dir / "result.jsonl"
    bench_args = _common_bench_args(args, batch_size, result_path)

    apply_summary_path = None
    fallback_summary_path = None
    if config_name == "original":
        command = [args.python, "-m", "sglang.bench_one_batch", *bench_args]
    elif config_name == "fallback_substitution":
        fallback_summary_path = run_dir / "fallback_summary.json"
        command = [
            args.python,
            str(FALLBACK_BOOTSTRAP),
            "--summary-json",
            str(fallback_summary_path),
            "--flashinfer-workspace-base",
            str(Path(args.flashinfer_workspace_base).resolve()),
            "--module",
            "sglang.bench_one_batch",
            "--",
            *bench_args,
        ]
    elif config_name == "your_cuda_kernel_with_apply":
        apply_summary_path = run_dir / "apply_summary.json"
        command = [
            args.python,
            str(APPLY_BOOTSTRAP),
            "--trace-set-path",
            str(Path(args.trace_set_path).resolve()),
            "--flashinfer-workspace-base",
            str(Path(args.flashinfer_workspace_base).resolve()),
            "--trace-hardware-contains",
            args.trace_hardware_contains,
            "--solution-language",
            "cuda",
            "--adapter-scope",
            "rmsnorm_only",
            "--only-definition",
            args.definition,
            "--solution-pool",
            "generated_only",
            "--on-miss-policy",
            "use_def_best",
            "--max-atol",
            str(args.max_atol),
            "--max-rtol",
            str(args.max_rtol),
            "--apply-summary-json",
            str(apply_summary_path),
            "--module",
            "sglang.bench_one_batch",
            "--",
            *bench_args,
        ]
        if args.fib_cache_path:
            command[2:2] = ["--fib-cache-path", str(Path(args.fib_cache_path).resolve())]
    else:
        raise ValueError(f"Unsupported e2e config: {config_name}")

    run_info = run_command(
        command,
        cwd=SCRIPT_DIR,
        env=_build_run_env(args),
        stdout_path=stdout_path,
        stderr_path=stderr_path,
    )
    row: Dict[str, Any] = {
        "kind": "e2e",
        "config": config_name,
        "batch_size": batch_size,
        "status": run_info["status"],
        "exit_code": run_info["exit_code"],
        "duration_s": run_info["duration_s"],
        "command": shlex.join(command),
        "stdout_log": str(stdout_path),
        "stderr_log": str(stderr_path),
        "result_path": str(result_path),
        "apply_summary_json": str(apply_summary_path) if apply_summary_path else "",
        "fallback_summary_json": str(fallback_summary_path) if fallback_summary_path else "",
    }
    if result_path.exists():
        payload = load_json_artifact(result_path, fmt="jsonl") or {}
        row.update(
            {
                "prefill_latency_s": payload.get("prefill_latency"),
                "total_latency_s": payload.get("total_latency"),
                "prefill_throughput": payload.get("prefill_throughput"),
                "overall_throughput": payload.get("overall_throughput"),
                "input_len": payload.get("input_len"),
                "output_len": payload.get("output_len"),
            }
        )
    if apply_summary_path and apply_summary_path.exists():
        apply_summary = load_json_file(apply_summary_path)
        row["selected_solutions"] = ";".join(
            f"{item['solution']}:{item['calls']}" for item in apply_summary.get("selected_solutions", [])
        )
        row["table_hit_calls"] = apply_summary["dispatch_stats"]["table_hit_calls"]
        row["def_best_calls"] = apply_summary["dispatch_stats"]["def_best_calls"]
        row["fallback_calls"] = apply_summary["dispatch_stats"]["fallback_calls"]
    if fallback_summary_path and fallback_summary_path.exists():
        fallback_summary = load_json_file(fallback_summary_path)
        row["fallback_mode"] = fallback_summary.get("mode", "")
        row["fallback_replacement_entrypoint"] = fallback_summary.get("replacement_entrypoint", "")
    return row


def main() -> None:
    args = build_parser().parse_args()
    output_dir = Path(args.output_dir).resolve()
    output_dir.mkdir(parents=True, exist_ok=True)

    kernel_rows: List[Dict[str, Any]] = []
    e2e_rows: List[Dict[str, Any]] = []

    for batch_size in args.batch_sizes:
        kernel_rows.append(
            _run_kernel_case(args, batch_size=batch_size, config_name="flashinfer_baseline", output_dir=output_dir)
        )
        kernel_rows.append(
            _run_kernel_case(args, batch_size=batch_size, config_name="your_cuda_kernel", output_dir=output_dir)
        )

    for batch_size in args.batch_sizes:
        e2e_rows.append(
            _run_e2e_case(args, batch_size=batch_size, config_name="original", output_dir=output_dir)
        )
        e2e_rows.append(
            _run_e2e_case(
                args,
                batch_size=batch_size,
                config_name="fallback_substitution",
                output_dir=output_dir,
            )
        )
        e2e_rows.append(
            _run_e2e_case(
                args,
                batch_size=batch_size,
                config_name="your_cuda_kernel_with_apply",
                output_dir=output_dir,
            )
        )

    manifest = {
        "model_path": str(Path(args.model_path).resolve()),
        "trace_set_path": str(Path(args.trace_set_path).resolve()),
        "definition": args.definition,
        "solution_name": args.solution_name,
        "batch_sizes": args.batch_sizes,
        "input_len": args.input_len,
        "output_len": args.output_len,
        "device": args.device,
        "dtype": args.dtype,
        "hidden_size": args.hidden_size,
        "trace_hardware_contains": args.trace_hardware_contains,
        "max_atol": args.max_atol,
        "max_rtol": args.max_rtol,
        "attention_backend": args.attention_backend,
        "sampling_backend": args.sampling_backend,
        "disable_cuda_graph": args.disable_cuda_graph,
        "flashinfer_workspace_base": str(Path(args.flashinfer_workspace_base).resolve()),
        "kernel_configs": ["flashinfer_baseline", "your_cuda_kernel"],
        "e2e_configs": ["original", "fallback_substitution", "your_cuda_kernel_with_apply"],
    }

    write_json(output_dir / "manifest.json", manifest)
    write_csv(output_dir / "kernel_latency.csv", kernel_rows)
    write_csv(output_dir / "e2e_latency.csv", e2e_rows)
    write_json(output_dir / "kernel_latency.json", kernel_rows)
    write_json(output_dir / "e2e_latency.json", e2e_rows)


if __name__ == "__main__":
    main()
